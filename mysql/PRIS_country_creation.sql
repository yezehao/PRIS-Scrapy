CREATE TABLE pris.PRIS_country (
    country VARCHAR(20),
    `reactor number in operation` INT,
    `reactor number suspended` INT,
    `reactor number under construction` INT,
    `reactor number permanent shutdown` INT,
    `Total Net Electrical Capacity [MW] in operation` INT,
    `Total Net Electrical Capacity [MW] suspended` INT,
    `Total Net Electrical Capacity [MW] under construction` INT,
    `Total Net Electrical Capacity [MW] permanent shutdown` INT,
    `2020-2022 EAF [%]` INT,
    `2020-2022 UCF [%]` INT,
    `2020-2022 UCL [%]` INT,
    `lifetime EAF [%] up to 2022` INT,
    `lifetime UCF [%] up to 2022` INT,
    `lifetime UCL [%] up to 2022` INT,
    `Nuclear Electricity Supplied [GW.h]` INT,
    `Nuclear Share [%]` INT
);